package com.javacode.day2;

public class Increment_Decrement_Operator {

	public static void main(String[] args) {
		// ++ is known as increment operator

		// -- is known as decrement operator

				

		int i = 10;

		i++; // post incremental operator

		++i; // pre increment operator

		i--; // post decrement operator

		--i; // pre decrement operator

	}

}
