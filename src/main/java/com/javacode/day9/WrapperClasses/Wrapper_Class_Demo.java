package com.javacode.day9.WrapperClasses;

public class Wrapper_Class_Demo {
	
	//int     -Integer
	//byte    -Byte
	//short   -Short
	//long    -Long
	//double  -Double
	//float   -Float
	//boolean -Boolean
	//char    -Character
	
	//What are Wrapper Classes?
	//Pre defined classes in java which can contain primitive data type.
	//They wrap primitive data to wrapper class objects.

	public static void main(String[] args) {
		
		int a = 10;
		System.out.println("The value of the primitive data type is" + a);
		
		Integer b = 100;
		System.out.println("The value of the primitive data type is" + b);
	}

}
